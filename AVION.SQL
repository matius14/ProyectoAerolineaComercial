CREATE TABLE PIODB.AVION
(
   ID_AVION NUMBER(7) NOT NULL,
   REFERNCIA VARCHAR2(10)NOT NULL,
   NOMBRE VARCHAR2(50),
   MODELO NUMBER(4) NOT NULL,
   CAPACIDAD INTEGER,
   ID_TRIPULACION INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL
)TABLESPACE PIO;



COMMENT ON TABLE PIODB.AVION IS 'AVION';



COMMENT ON COLUMN PIODB.AVION.ID_AVION  IS 'ID_AVION';


COMMENT ON COLUMN PIODB.AVION.REFERNCIA  IS 'REFERNCIA DEL AVION';

COMMENT ON COLUMN PIODB.AVION.NOMBRE  IS 'NOMBRE DEL AVION';


COMMENT ON COLUMN PIODB.AVION.MODELO  IS 'MODELO DEL AVION';


COMMENT ON COLUMN PIODB.AVION.CAPACIDAD  IS 'CAPACIDAD DEL AVION';

COMMENT ON COLUMN PIODB.AVION.ID_TRIPULACION  IS 'TRIPULACION DEL AVION';



CREATE UNIQUE INDEX IDX_PK_ID_AVION_AVION
       ON PIODB.AVION(ID_AVION)
       TABLESPACE PIO_INDEXES ;

ALTER TABLE PIODB.AVION ADD CONSTRAINT PK_ID_AVION_AVION PRIMARY KEY (ID_AVION)
       USING INDEX IDX_PK_ID_AVION_AVION;
      
ALTER TABLE PIODB.AVION ADD CONSTRAINT FK_AVION_TRIPULACION
     FOREIGN KEY (ID_TRIPULACION) REFERENCES PIODB.TIPULACION(ID_TRIPULACION);
           
