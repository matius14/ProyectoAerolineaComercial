

CREATE TABLE PIODB.EMPLEADO
(
    
     NUMEROCEDULA NUMBER(10) NOT NULL,
     PRIMERNOMBRE VARCHAR2(60)NOT NULL,
     SEGUNDONOMBRE VARCHAR2(60),
     PRIMERAPELLIDO VARCHAR2(60) NOT NULL,
     SEGUNDOAPELLIDO VARCHAR2(60),
     NUMEROTELEFONO NUMBER(10)NOT NULL,
     CORREO VARCHAR2(60)NOT NULL,
     CARGO VARCHAR2(100) NOT NULL
     

) TABLESPACE PIO;



COMMENT ON TABLE PIODB.EMPLEADO IS 'PASAJERO';



COMMENT ON COLUMN PIODB.EMPLEADO.NUMEROCEDULA  IS 'NUMERO DE CEDULA DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.PRIMERNOMBRE  IS 'PRIMER NOMBRE DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.SEGUNDONOMBRE  IS 'SEGUNDO NOMBRE DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.PRIMERAPELLIDO  IS 'PRIMER APELLIDO DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.SEGUNDOAPELLIDO  IS 'SEGUNDO APELLIDO DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.NUMEROTELEFONO  IS 'NUMERO DE TELEFONO DEL PASAJERO';

COMMENT ON COLUMN PIODB.EMPLEADO.CORREO  IS 'CORREO DEL EMPLEADO';

COMMENT ON COLUMN PIODB.EMPLEADO.CARGO  IS 'CARGO DEL EMPLEADO';


-- INDICE

CREATE UNIQUE INDEX IDX_PK_NUMEROCEDULA_EMPLEADO
       ON PIODB.EMPLEADO(NUMEROCEDULA)
       TABLESPACE PIO_INDEXES ;
       
             
       
ALTER TABLE PIODB.EMPLEADO ADD CONSTRAINT PK_NUMEROCEDULA_EMPLEADO PRIMARY KEY (NUMEROCEDULA)
       USING INDEX IDX_PK_NUMEROCEDULA_EMPLEADO;

