


CREATE TABLE PIODB.TIPULACION
(
   ID_TRIPULACION INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   CODIGO NUMBER(5)NOT NULL ,
   NUMEROCEDULA NUMBER(10) NOT NULL

)TABLESPACE PIO;


COMMENT ON TABLE PIODB.TIPULACION IS 'TRIPULACION';



COMMENT ON COLUMN PIODB.TIPULACION.ID_TRIPULACION  IS 'ID TRIPULACION';


COMMENT ON COLUMN PIODB.TIPULACION.CODIGO  IS 'CODIGO DE LA  TRIPULACION';

COMMENT ON COLUMN PIODB.TIPULACION.NUMEROCEDULA IS 'CEDULA DEL EMPLEADO';



CREATE UNIQUE INDEX IDX_PK_ID_TRIPULACION_TIPULACION
       ON PIODB.TIPULACION(ID_TRIPULACION)
       TABLESPACE PIO_INDEXES ;
       
       
ALTER TABLE PIODB.TIPULACION ADD CONSTRAINT PK_ID_TRIPULACION_TIPULACION PRIMARY KEY (ID_TRIPULACION)
       USING INDEX IDX_PK_ID_TRIPULACION_TIPULACION;
       
       
       
ALTER TABLE PIODB.TIPULACION ADD CONSTRAINT FK_TIPULACION_EMPLEADO
     FOREIGN KEY (NUMEROCEDULA) REFERENCES PIODB.EMPLEADO(NUMEROCEDULA);
          