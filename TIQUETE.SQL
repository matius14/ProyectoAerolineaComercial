CREATE TABLE PIODB.TIQUETE
(
   ID_TIQUETE NUMBER(10),
   FECHA DATE,
   ID_PASAJERO INTEGER GENERATED BY DEFAULT AS IDENTITY
)TABLESPACE PIO ;



COMMENT ON TABLE PIODB.TIQUETE IS 'TIQUETE';


COMMENT ON COLUMN PIODB.TIQUETE.ID_TIQUETE  IS 'ID DEL TIQUETE';

COMMENT ON COLUMN PIODB.TIQUETE.FECHA  IS 'FECHA DE TIQUETE';

COMMENT ON COLUMN PIODB.TIQUETE.ID_PASAJERO  IS 'ID DEL PASAJERO';

--INDEX

CREATE UNIQUE INDEX IDX_PK_ID_TIQUETE_TIQUETE
    ON PIODB.TIQUETE(ID_TIQUETE)
    TABLESPACE PIO_INDEXES ;
    
ALTER TABLE PIODB.TIQUETE ADD CONSTRAINT PK_ID_TIQUETE_TIQUETE PRIMARY KEY (ID_TIQUETE)
      USING INDEX IDX_PK_ID_TIQUETE_TIQUETE ;

ALTER TABLE PIODB.TIQUETE ADD CONSTRAINT FK_TIQUETE_PASAJERO
     FOREIGN KEY (ID_PASAJERO) REFERENCES PIODB.PASAJERO(ID_PASAJERO);
     