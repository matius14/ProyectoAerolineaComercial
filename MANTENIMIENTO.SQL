

CREATE TABLE PIODB.MATENIMIENTO
(
   ID_MANTENIMIENTO INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   DESCRIPCION VARCHAR2(1000) NOT NULL,
   ID_AVION NUMBER(7) NOT NULL
   
)TABLESPACE PIO;


COMMENT ON TABLE PIODB.MATENIMIENTO IS 'MATENIMIENTO';


COMMENT ON COLUMN PIODB.MATENIMIENTO.ID_MANTENIMIENTO  IS 'ID_MANTENIMIENTO DEL AVION';

COMMENT ON COLUMN PIODB.MATENIMIENTO.DESCRIPCION  IS 'DESCRIPCION DEL AVION';


COMMENT ON COLUMN PIODB.MATENIMIENTO.ID_AVION  IS 'ID_AVION DEL AVION';



CREATE UNIQUE INDEX IDX_PK_ID_MANTENIMIENTO_MATENIMIENTO
       ON PIODB.MATENIMIENTO(ID_MANTENIMIENTO)
       TABLESPACE PIO_INDEXES ;


ALTER TABLE PIODB.MATENIMIENTO ADD CONSTRAINT PK_ID_MANTENIMIENTO_MATENIMIENTO PRIMARY KEY (ID_MANTENIMIENTO)
       USING INDEX IDX_PK_ID_MANTENIMIENTO_MATENIMIENTO;
       
       
       
ALTER TABLE PIODB.MATENIMIENTO ADD CONSTRAINT FK_MANTENIMIENTO_AVION
     FOREIGN KEY (ID_AVION) REFERENCES PIODB.AVION(ID_AVION);
           