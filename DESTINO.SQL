CREATE TABLE PIODB.DESTINO
(  

    ID_DESTINO NUMBER(10) NOT NULL,
    PAIS_LLEGADA VARCHAR2(50) NOT NULL,
    CIUDAD_LLEGADA VARCHAR2(50) NOT NULL,
    ID_AEROPURTO NUMBER(5) NOT NULL,


)
TABLESPACE PIO;


COMMENT ON TABLE PIODB.DESTINO IS 'DESTINO';

COMMENT ON COLUMN PIODB.DESTINO.PAIS_LLEGADA  IS 'PAIS LLEGADA';

COMMENT ON COLUMN PIODB.DESTINO.CIUDAD_LLEGADA  IS 'CIUDAD LLEGADA';


COMMENT ON COLUMN PIODB.DESTINO.ID_AEROPURTO  IS 'ID DEL ID DEL AEROPUERTO';


--INDICE

CREATE UNIQUE INDEX IDX_PK_ID_DESTINO_DESTINO
      ON PIODB.DESTINO(ID_DESTINO)
      TABLESPACE PIO_INDEXES;



ALTER TABLE PIODB.DESTINO ADD CONSTRAINT PK_ID_DESTINO_DESTINO PRIMARY KEY (ID_DESTINO)
    USING INDEX IDX_PK_ID_DESTINO_DESTINO;


ALTER TABLE PIODB.DESTINO ADD CONSTRAINT FK_DESTINO_AEROPUERTO
	FOREIGN KEY (ID_AEROPURTO) REFERENCES PIODB.AEROPUERTO(ID_AEROPURTO);
