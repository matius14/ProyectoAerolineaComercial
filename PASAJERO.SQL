

CREATE TABLE PIODB.PASAJERO
(
     ID_PASAJERO INTEGER GENERATED BY DEFAULT AS IDENTITY,
     NUMEROCEDULA NUMBER(10) NOT NULL,
     PRIMERNOMBRE VARCHAR2(60)NOT NULL,
     SEGUNDONOMBRE VARCHAR2(60),
     PRIMERAPELLIDO VARCHAR2(60) NOT NULL,
     SEGUNDOAPELLIDO VARCHAR2(60),
     NUMEROTELEFONO NUMBER(10)NOT NULL,
     CORREO VARCHAR2(60)NOT NULL
     

) TABLESPACE PIO;



COMMENT ON TABLE PIODB.PASAJERO IS 'PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.ID_PASAJERO  IS 'ID DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.NUMEROCEDULA  IS 'NUMERO DE CEDULA DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.PRIMERNOMBRE  IS 'PRIMER NOMBRE DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.SEGUNDONOMBRE  IS 'SEGUNDO NOMBRE DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.PRIMERAPELLIDO  IS 'PRIMER APELLIDO DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.SEGUNDOAPELLIDO  IS 'SEGUNDO APELLIDO DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.NUMEROTELEFONO  IS 'NUMERO DE TELEFONO DEL PASAJERO';

COMMENT ON COLUMN PIODB.PASAJERO.CORREO  IS 'CORREO DEL PASAJERO';


-- INDICE

CREATE UNIQUE INDEX IDX_PK_ID_PASAJERO_PASAJERO
       ON PIODB.PASAJERO(ID_PASAJERO)
       TABLESPACE PIO_INDEXES ;
       
             
       
